import Foundation

public class AscendConfig {
  
  func getDomain() -> String {
    let domain: String = ""
    return domain;
  }
  
  func getVersion() -> String {
    let version: String = ""
    return version;
  }
  
  func getEnvironmentId() -> String {
    let environmentId: String = ""
    return environmentId;
  }

}
